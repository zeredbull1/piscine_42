int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	i;
	int	count;

	(void) length;
	count = 0;
	i = 0;
	while (tab[i])
	{
		if (f(tab[i++]) != 0)
			count++;
	}
	return (count);
}

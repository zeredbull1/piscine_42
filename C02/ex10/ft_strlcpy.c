int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	int	i;

	i = 0;
	size = 1;
	while (dest[i] != '\0' && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	i = 0;
	while (src[i] != '\0')
	{
		i++;
	}
	return (i++);
}

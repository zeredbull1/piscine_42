#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*ptr;

	i = 0;
	while (src[i++] != '\0')
		;
	ptr = malloc(sizeof(src) * i);
	while (i >= 0)
	{
		ptr[i] = src[i];
		i--;
	}
	return (ptr);
}

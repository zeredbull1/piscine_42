int	ft_sqrt(int nb)
{
	int	i;
	int	limit;
	int	h;

	if (nb <= 0)
		return (0);
	limit = 100;
	i = 0;
	h = (1 + nb / 1) / 2;
	while (i < limit)
	{
		h = (h + nb / h) / 2;
		if ((h * h) == nb)
			return (h);
		i++;
	}
	return (0);
}
